namespace Mznqa
{
    /*!
     * \enum    Direction
     *
     * \brief   枚举正负方向
     */

    public enum Direction
    {
        Positive,   ///< 正方向
        Negative    ///< 负方向
    }

    /*!
     * \enum    Direction2
     *
     * \brief   枚举两方向
     */

    public enum Direction2
    {
        Horizontal, ///< 水平方向
        Vertical    ///< 垂直方向
    }

    /*!
     * \enum    Direction4
     *
     * \brief   枚举四方向
     */

    public enum Direction4
    {
        Up,     ///< 上
        Right,  ///< 右
        Down,   ///< 下
        Left    ///< 左
    }
}